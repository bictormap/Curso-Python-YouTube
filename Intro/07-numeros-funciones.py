import math

print(round(1.3))
print(round(1.5))
print(round(1.7))
print(abs(-77))
print(abs(55))

print(math.ceil(1.1))
print(math.floor(1.99999))
# "isnan" devuelve un True solo cuando el valor ingresado no es un numero.
print(math.isnan(23))
# print(math.isnan("23"))

# "pow" es utilizado para elevar a la potencia de
print(math.pow(10, 3))
# "sqrt" es utilizado para buscar la raiz cuadrada de
print(math.sqrt(9))
